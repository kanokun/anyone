$(".btn-video-skip").click(function(){

	$("#video-intro").get(0).pause();
	$(".outer-video").addClass("hide");
	$(".outer-slide").fadeIn(800);

	$(window).resize();
});

$(".btn-video-mute").click(function(){

	if($(".btn-video-mute i").hasClass("fa-volume-off")){
		
		$(".btn-video-mute i").addClass("fa-volume-down");
		$(".btn-video-mute i").removeClass("fa-volume-off");

		$(".btn-video-mute span").html(" 음소거");

		$("#video-intro").get(0).volume = 1;
		
	}else{
		
		$(".btn-video-mute i").addClass("fa-volume-off");
		$(".btn-video-mute i").removeClass("fa-volume-down");

		$(".btn-video-mute span").html(" 볼륨켜기");

		$("#video-intro").get(0).volume = 0;
	}
});

